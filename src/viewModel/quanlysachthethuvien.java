public class quanlysachthethuvien {
   private String maphieu;
   private String masach;
   private String tensach;
   private String ngaymuon;
   private String ngaytra;
   private String tinhtrangsach;
   private String hoten;

    public quanlysachthethuvien() {
    }

    public quanlysachthethuvien(String maphieu, String masach, String tensach, String ngaymuon, String ngaytra, String tinhtrangsach, String hoten) {
        this.maphieu = maphieu;
        this.masach = masach;
        this.tensach = tensach;
        this.ngaymuon = ngaymuon;
        this.ngaytra = ngaytra;
        this.tinhtrangsach = tinhtrangsach;
        this.hoten = hoten;
    }

    public String getMaphieu() {
        return maphieu;
    }

    public void setMaphieu(String maphieu) {
        this.maphieu = maphieu;
    }

    public String getMasach() {
        return masach;
    }

    public void setMasach(String masach) {
        this.masach = masach;
    }

    public String getTensach() {
        return tensach;
    }

    public void setTensach(String tensach) {
        this.tensach = tensach;
    }

    public String getNgaymuon() {
        return ngaymuon;
    }

    public void setNgaymuon(String ngaymuon) {
        this.ngaymuon = ngaymuon;
    }

    public String getNgaytra() {
        return ngaytra;
    }

    public void setNgaytra(String ngaytra) {
        this.ngaytra = ngaytra;
    }

    public String getTinhtrangsach() {
        return tinhtrangsach;
    }

    public void setTinhtrangsach(String tinhtrangsach) {
        this.tinhtrangsach = tinhtrangsach;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    
    

    
   
}
