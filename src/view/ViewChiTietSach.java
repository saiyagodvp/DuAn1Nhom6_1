    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        int choose = JOptionPane.showConfirmDialog(this, "Bạn có muốn thêm không", "Choose", JOptionPane.YES_NO_CANCEL_OPTION);
        String maISBN = txtMaISBN.getText();
        String doHuHao = "";

        boolean trangthai = true;
        if (rdbChuDuocMuon.isSelected()) {
            trangthai = false;
        } else if (rdbDaMuon.isSelected()) {
            trangthai = true;
        }
        boolean tinhtrang = false;
        if (rdbConMoi.isSelected()) {
            tinhtrang = true;
            doHuHao = "100";
        } else if (rdbCu.isSelected()) {
            tinhtrang = false;
            doHuHao = txtDoHuHao.getText();
        }
        ChiTietSach ctd = chiTietSachSevice.getOne(maISBN);
        if (maISBN.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã ISBN của cuốn sách rồi thử lại");
        }else if(ctd != null){
            JOptionPane.showMessageDialog(this, "Mã ISBN đã tồn tại");
        }
        else if (doHuHao.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập độ hư hao của cuốn sách");
        } else {
            if (choose == JOptionPane.YES_OPTION) {
                ChiTietSach cts = new ChiTietSach(id, maISBN, Float.valueOf(doHuHao), trangthai, tinhtrang);
                JOptionPane.showMessageDialog(this, this.chiTietSachSevice.add(cts, masach));
                loadDataToTable();
            }

        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        int choose = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không", "Choose", JOptionPane.YES_NO_CANCEL_OPTION);
        String maISBN = txtMaISBN.getText();
        String doHuHao = "";
        String maS = tbListCTSach.getValueAt(tbListCTSach.getSelectedRow(), 0).toString();
        boolean trangthai = true;
        if (rdbChuDuocMuon.isSelected()) {
            trangthai = false;
        } else if (rdbDaMuon.isSelected()) {
            trangthai = true;
        }
        boolean tinhtrang = false;
        if (rdbConMoi.isSelected()) {
            tinhtrang = true;
            doHuHao = "100";
        } else if (rdbCu.isSelected()) {
            tinhtrang = false;
            doHuHao = txtDoHuHao.getText();
        }
        if (maISBN.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã ISBN của cuốn sách rồi thử lại");
        } else if (doHuHao.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập độ hư hao của cuốn sách");
        } else if (Integer.parseInt(doHuHao) < 20) {
            JOptionPane.showMessageDialog(this, "Cuốn sách đã quá cũ không dùng được nữa");
        } else if (Integer.parseInt(doHuHao) > 100) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng độ hư hao");
        } else {
            if (choose == JOptionPane.YES_OPTION) {
                ChiTietSach cts = new ChiTietSach(id, maISBN, Float.valueOf(doHuHao), trangthai, tinhtrang);
                JOptionPane.showMessageDialog(this, this.chiTietSachSevice.update(cts, maS));
                loadDataToTable();
            }
        }

    }//GEN-LAST:event_btnCapNhatActionPerformed

