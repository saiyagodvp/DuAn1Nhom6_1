    private void loadDataToTable() {
        listCT = chiTietSachSevice.getAll(id);

        DefaultTableModel dtm = (DefaultTableModel) this.tbListCTSach.getModel();
        dtm.setRowCount(0);
        for (ChiTietSach x : listCT) {
            Object[] rowData = {
                x.getMaQuyenSach(), x.getMaISBN(), x.isTinhTrang() ? "Còn Mới" : "Đã Cũ", x.getDoHuHao(), x.isTrangThai() ? "Đã Mượn" : "Chưa Được Mượn"
            };

            dtm.addRow(rowData);
        }
    }

    public void loadSP(int i) {
        listCT = chiTietSachSevice.getAll(id);
        ChiTietSach qlnv = listCT.get(i);
        lbNameSach.setText(tensach);
        lbMaQuyenSach.setText(qlnv.getMaQuyenSach());
        txtMaISBN.setText(qlnv.getMaISBN());
        txtDoHuHao.setText(String.valueOf(qlnv.getDoHuHao()));
        if (qlnv.isTrangThai()) {
            rdbDaMuon.setSelected(true);
        } else {
            rdbChuDuocMuon.setSelected(false);
        }
        if (qlnv.isTinhTrang()) {
            rdbConMoi.setSelected(true);
        } else {
            rdbCu.setSelected(false);
        }
    }

